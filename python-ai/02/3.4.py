# 创建集合
s = {1, 2, 3, 3, 4, 5}
print(s)  # 输出: {1, 2, 3, 4, 5}

# 添加元素
s.add(6)
print(s)  # 输出: {1, 2, 3, 4, 5, 6}

# 移除元素
s.remove(3)
print(s)  # 输出: {1, 2, 4, 5, 6}

# 集合运算
a = {1, 2, 3, 4}
b = {3, 4, 5, 6}

print(a | b)  # 并集: {1, 2, 3, 4, 5, 6}
print(a & b)  # 交集: {3, 4}
print(a - b)  # 差集: {1, 2}
print(a ^ b)  # 对称差集: {1, 2, 5, 6}